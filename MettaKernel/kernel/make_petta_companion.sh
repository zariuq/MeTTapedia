#!/usr/bin/env bash
# make_petta_companion.sh — generate a PeTTa-runnable companion from a core-MeTTa kernel file.
#
# This is a LEXICAL transform, deliberately NOT the semantic he_to_petta.pl translator
# (that one is for HE *programs* with chain/eval/switch and wraps bodies in call-or-inert;
#  kernel files are core MeTTa and only need PeTTa lexical safety):
#   1. hyphen-in-identifier  -> underscore   ('-' is a Prolog operator in PeTTa)
#   2. !(assertEqual A B)     -> !(test A B)   (PeTTa-native assertion)
#   3. prepend the lib_he import IFF the source uses `unify` (lib_he provides it)
#
# Atomic: writes a temp file then mv's it into place, so an interrupt/failure never
# leaves a half-written or zero-byte output. Non-destructive by default: emits
# <name>_petta_gen.metta and does NOT overwrite a hand-made <name>_petta.metta.
#
# Usage: make_petta_companion.sh <input.metta> [output.metta]
set -euo pipefail
LIB_HE='/home/aimama/aihub/hyperon/PeTTa/lib/lib_he'

src="${1:?usage: make_petta_companion.sh <input.metta> [output.metta]}"
[ -f "$src" ] || { echo "error: no such file: $src" >&2; exit 2; }
dir=$(cd "$(dirname "$src")" && pwd)
base=$(basename "$src" .metta)
out="${2:-$dir/${base}_petta_gen.metta}"

uses_unify=no
grep -qE '\bunify\b' "$src" && uses_unify=yes

# temp file in the SAME directory so the final mv is atomic (same filesystem)
tmp=$(mktemp "$dir/.${base}_petta_gen.XXXXXX.tmp")
trap 'rm -f "$tmp"' EXIT

{
  echo "; AUTO-GENERATED by make_petta_companion.sh from $(basename "$src") — do not hand-edit."
  echo "; Transform: hyphen->underscore identifiers; assertEqual->test; lib_he import iff unify used."
  [ "$uses_unify" = yes ] && echo "!(import! &self $LIB_HE)"
  echo ""
  # :a;...;ta loops the substitution so multi-hyphen names (decl-type-of) fully convert.
  # The pattern requires alphanumerics on BOTH sides of '-', so the arithmetic minus
  # operator "(- $k 1)" (spaces around it) is left untouched.
  sed -E ':a; s/([A-Za-z0-9])-([A-Za-z0-9])/\1_\2/g; ta; s/\bassertEqual\b/test/g' "$src"
} > "$tmp"

mv -f "$tmp" "$out"   # atomic replace
trap - EXIT
echo "wrote $out ($(wc -l < "$out") lines; lib_he=$uses_unify)"
